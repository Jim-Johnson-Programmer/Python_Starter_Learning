# 11-logical-operators
# Python learning exercise

#use of logical operators: and, or, not

# and operator
a = 5
b = 10
if a > 0 and b > 0:
    print("Both a and b are positive numbers.")

# or operator
c = -3
if a > 0 or c > 0:
    print("At least one of a or c is a positive number.")

# not operator
d = 0
if not d > 0:
    print("d is not a positive number.")

