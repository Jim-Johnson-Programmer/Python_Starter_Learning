# 1-variables
# Python learning exercise

x = "Hello"        # str
print(x)

y = 42             # int
print(y)

z = 3.14           # float
print(z)

c = 2 + 5j         # complex
print(c)

items = [1, 2, 3]  # list
print(items)

coords = (4, 5)    # tuple
print(coords)

settings = {"theme": "dark"}  # dict
print(settings)

unique = {1, 2, 3} # set
print(unique)

flag = True        # bool
print(flag)

data = b"ABC"      # bytes
print(data)

nothing = None     # NoneType
print(nothing)
